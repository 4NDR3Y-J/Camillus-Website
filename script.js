function searchStudents() {
    var input, filter, cards, cardContainer, h3, i, txtValue;
    input = document.getElementById('studentSearch');
    filter = input.value.toLowerCase().trim();
    cardContainer = document.getElementById("student-list");
    cards = cardContainer.getElementsByClassName('student-card');

    for (i = 0; i < cards.length; i++) {
        h3 = cards[i].getElementsByTagName("h3")[0];
        txtValue = h3.textContent || h3.innerText;

        if (txtValue.toLowerCase().trim().includes(filter)) {
            cards[i].style.display = "";
        } else {
            cards[i].style.display = "none";
        }
    }
}

    function showSection(sectionId) {
        var sections = document.querySelectorAll('.section');
        sections.forEach(section => {
            section.classList.remove('active');
            setTimeout(() => section.style.display = 'none', 500);
        });

        var activeSection = document.getElementById(sectionId);
        setTimeout(() => {
            activeSection.style.display = 'block';
            setTimeout(() => activeSection.classList.add('active'), 50);
        }, 500);
}

    function toggleFolder(folderId) {
        var folder = document.getElementById(folderId);
        if (folder.style.display === "none") {
            folder.style.display = "block";
        } else {
            folder.style.display = "none";
        }
}

    let currentImageIndex = 0;
    let images = [];
    const modal = document.getElementById('imageModal');
    const modalImage = document.getElementById('modalImage');

    function openModal(index) {
        images = document.querySelectorAll('.pictures-container img');
        currentImageIndex = index;
        modal.style.display = "block";
        modalImage.src = images[currentImageIndex].src;
        console.log('Modal opened. Current image index:', currentImageIndex);
}

    function closeModal() {
        modal.style.display = "none";
        console.log('Modal closed.');
}

    function changeImage(direction) {
        currentImageIndex += direction;
        if (currentImageIndex < 0) {
            currentImageIndex = images.length - 1;
        } else if (currentImageIndex >= images.length) {
            currentImageIndex = 0;
        }
        modalImage.src = images[currentImageIndex].src;
        console.log('Image changed. Current image index:', currentImageIndex);
}

    document.addEventListener('DOMContentLoaded', () => {
        modal.style.display = 'none';
        document.querySelector('.prev').onclick = () => changeImage(-1);
        document.querySelector('.next').onclick = () => changeImage(1);
        console.log('Event listeners for arrows added.')
});